/// 根据汉字获取五笔编码
pub fn get_wubi_code(character: &str) -> Option<&'static str> {
    get_all_entries()
        .iter()
        .find(|entry| entry.character == character)
        .map(|entry| entry.code)
}

/// 根据汉字获取拼音
pub fn get_pinyin(character: &str) -> Option<&'static str> {
    get_all_entries()
        .iter()
        .find(|entry| entry.character == character)
        .map(|entry| entry.pinyin)
}
